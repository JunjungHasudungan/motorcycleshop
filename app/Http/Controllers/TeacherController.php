     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => 'required|min:4',
            ]);
        
         Teacher::create([
            'name' => $request->name,
            'slug' =>Str::slug($request->name, '-'),
        ]);

        return redirect()->route('teacher.index'); 
    }

